// Deletes middle of linked list and returns head of the modified list
class Solution{
    public:
    Node* deleteMid(Node* head)
    {
        // Your Code Here
        struct Node *p = head;
         struct Node *curr = head;
         struct Node *prev = NULL;
        int count = 0;
        if(p==NULL){
            return NULL;
        }
        while(p!=NULL){
             count +=1;
             p=p->next;
        }
        if(count == 1){
            return NULL;
        }
       int mid = count/2;
         for (int i = 1; i <=mid ; i++)
            {     
               prev=curr;
                curr= curr->next;
            }
          delete curr;
          prev->next=curr->next;
          
          
          
        return head;
    }
};

